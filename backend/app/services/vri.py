def normalize_vri(permitted_use: str | None) -> str:
    if not permitted_use:
        return "ДРУГОЕ"
    lower = permitted_use.lower()
    for code, keywords in VRI_RULES:
        if not keywords:
            continue
        for kw in keywords:
            if kw in lower:
                return code
    return "ДРУГОЕ"


